#include <stdio.h>

int main(){

	// Inicializacion  de variables
	unsigned int entero, auxiliar = 0;

	// Pedimos 
	printf("Ingresa un numero:\n");
	scanf("%x", &entero);		
	
	// Mostramos en pantalla
	auxiliar = entero >> 16 | entero << 16;
	printf("%X\n", auxiliar);
	
	return 0;
}
